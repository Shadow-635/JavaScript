//  Seven Days 

const day = 3;

switch (day) {
    case 1:
        console.log("IT is SaterDay")
        break;
    case 2:
        console.log("IT is SunDay")
        break;
    case 3:
        console.log("IT is MonDay")
        break;
    case 4:
        console.log("IT is TuesDay")
        break;
    case 5:
        console.log("IT is WednessDay")
        break;
    case 6:
        console.log("IT is ThuesDay")
        break;
    case 7:
        console.log("IT is FriDay")
        break;

    default:
        break;
}

const month = "mar"

